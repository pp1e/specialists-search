CREATE TABLE ROLE (
    ROLE_ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR(20) NOT NULL,
    CAN_WATCH_CONTACT_INFO BOOLEAN NOT NULL,
    CAN_CHANGE_CITY BOOLEAN NOT NULL,
    CAN_SUBMIT_REQUEST BOOLEAN NOT NULL,
    CAN_WATCH_SELF_REQUESTS BOOLEAN NOT NULL,
    CAN_OPERATE_SELF_REQUESTS BOOLEAN NOT NULL,
    CAN_WATCH_ALL_REQUESTS BOOLEAN NOT NULL,
    CAN_OPERATE_ALL_REQUESTS BOOLEAN NOT NULL,
    CAN_OPERATE_ADVERTS BOOLEAN NOT NULL
)